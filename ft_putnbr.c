#include "ft_printf.h"

void	ft_putnbr(int n, int *count)
{
	char	c;

	c = '0';
	if (n >= 0 && n <= 9)
	{
		c += n;
		write(1, &c, 1);
		*count += 1;
	}
	else if (n == -2147483648)
		write(1, "-2147483648", 11);
	else if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
		*count += 1;
		ft_putnbr(n, count);
	}
	else if (n > 9)
	{
		ft_putnbr(n / 10, count);
		ft_putnbr(n % 10, count);
	}
}
